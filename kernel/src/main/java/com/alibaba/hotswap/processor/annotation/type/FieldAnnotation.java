package com.alibaba.hotswap.processor.annotation.type;

/**
 * @author yong.zhuy 2012-6-18
 */
public class FieldAnnotation extends Annotation {

    protected String name;
    protected String desc;

    public FieldAnnotation(String className, String name, String desc){
        super(AnnotationType.FIELD, className);

        this.name = name;
        this.desc = desc;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
